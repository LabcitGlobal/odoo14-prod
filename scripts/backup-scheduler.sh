#!/bin/bash

# Script para programar backups automÃ¡ticos
# Configura crontab para backups regulares

set -e

SCRIPT_DIR="/home/carvajal/Desarrollo/odoo14-prod/scripts"
LOG_DIR="/home/carvajal/Desarrollo/odoo14-prod/logs"

# Colores para output
GREEN='\033[0;32m'
YELLOW='\033[1;33m'
BLUE='\033[0;34m'
NC='\033[0m'

log_info() {
    echo -e "${GREEN}[INFO]${NC} $1"
}

log_warn() {
    echo -e "${YELLOW}[WARN]${NC} $1"
}

log_question() {
    echo -e "${BLUE}[?]${NC} $1"
}

show_help() {
    echo "Configurador de backups automÃ¡ticos para Odoo 14"
    echo ""
    echo "Uso: $0 [opciÃ³n]"
    echo ""
    echo "Opciones:"
    echo "  install     Instalar tareas de backup automÃ¡tico"
    echo "  remove      Remover tareas de backup automÃ¡tico"
    echo "  status      Mostrar estado actual de backups programados"
    echo "  test        Ejecutar backup de prueba"
    echo ""
}

install_backups() {
    log_info "Configurando backups automÃ¡ticos..."
    
    # Crear directorio de logs si no existe
    mkdir -p "$LOG_DIR"
    
    # Backup completo: Domingos a las 2:00 AM
    FULL_BACKUP_CRON="0 2 * * 0 $SCRIPT_DIR/backup.sh >> $LOG_DIR/backup.log 2>&1"
    
    # Backup de BD: Lunes a SÃ¡bado a las 2:00 AM
    DB_BACKUP_CRON="0 2 * * 1-6 $SCRIPT_DIR/backup-db-only.sh >> $LOG_DIR/backup-db.log 2>&1"
    
    # Backup rÃ¡pido de BD: Cada 4 horas en dÃ­as laborables
    QUICK_BACKUP_CRON="0 */4 * * 1-5 $SCRIPT_DIR/backup-db-only.sh >> $LOG_DIR/backup-quick.log 2>&1"
    
    # Limpiar logs antiguos: Primer dÃ­a del mes
    CLEANUP_CRON="0 3 1 * * find $LOG_DIR -name '*.log' -mtime +30 -delete"
    
    log_info "Agregando tareas al crontab..."
    
    # Crear archivo temporal con las nuevas tareas
    TEMP_CRON=$(mktemp)
    
    # Obtener crontab actual (si existe) sin las tareas de Odoo
    crontab -l 2>/dev/null | grep -v "# Odoo Backup" | grep -v "$SCRIPT_DIR" > "$TEMP_CRON" || true
    
    # Agregar nuevas tareas
    cat >> "$TEMP_CRON" << EOF

# Odoo Backup Tasks - Generated by backup-scheduler.sh
# Backup completo semanal (Domingos 2:00 AM)
$FULL_BACKUP_CRON
# Backup de base de datos diario (Lunes-SÃ¡bado 2:00 AM)
$DB_BACKUP_CRON
# Backup rÃ¡pido cada 4 horas (dÃ­as laborables)
$QUICK_BACKUP_CRON
# Limpiar logs antiguos (primer dÃ­a del mes)
$CLEANUP_CRON
EOF
    
    # Instalar el nuevo crontab
    crontab "$TEMP_CRON"
    rm "$TEMP_CRON"
    
    log_info "âœ… Backups automÃ¡ticos configurados:"
    log_info "  ðŸ”„ Backup completo: Domingos 2:00 AM"
    log_info "  ðŸ’¾ Backup BD diario: Lunes-SÃ¡bado 2:00 AM"
    log_info "  âš¡ Backup rÃ¡pido: Cada 4 horas (dÃ­as laborables)"
    log_info "  ðŸ§¹ Limpieza de logs: Primer dÃ­a del mes"
    echo ""
    log_info "Logs se guardarÃ¡n en: $LOG_DIR"
}

remove_backups() {
    log_warn "Removiendo backups automÃ¡ticos..."
    
    # Crear archivo temporal sin las tareas de Odoo
    TEMP_CRON=$(mktemp)
    crontab -l 2>/dev/null | grep -v "# Odoo Backup" | grep -v "$SCRIPT_DIR" > "$TEMP_CRON" || true
    
    # Instalar el crontab limpio
    crontab "$TEMP_CRON"
    rm "$TEMP_CRON"
    
    log_info "âœ… Backups automÃ¡ticos removidos"
}

show_status() {
    log_info "Estado actual de backups programados:"
    echo ""
    
    if crontab -l 2>/dev/null | grep -q "$SCRIPT_DIR"; then
        log_info "âœ… Backups automÃ¡ticos ACTIVOS:"
        echo ""
        crontab -l | grep "$SCRIPT_DIR" | while read line; do
            echo "  $line"
        done
        echo ""
        
        # Mostrar prÃ³xima ejecuciÃ³n
        log_info "PrÃ³ximas ejecuciones programadas:"
        echo "  $(date -d 'next Sunday 2:00' '+%Y-%m-%d %H:%M') - Backup completo"
        echo "  $(date -d 'tomorrow 2:00' '+%Y-%m-%d %H:%M') - Backup de BD"
        
    else
        log_warn "âŒ No hay backups automÃ¡ticos configurados"
        log_info "Ejecuta: $0 install"
    fi
    
    echo ""
    log_info "Ãšltimos backups realizados:"
    ls -la /home/carvajal/Desarrollo/odoo14-prod/backups/*.tar.gz 2>/dev/null | tail -5 || log_warn "No hay backups disponibles"
    
    echo ""
    log_info "Espacio usado por backups:"
    du -sh /home/carvajal/Desarrollo/odoo14-prod/backups/ 2>/dev/null || log_warn "Directorio de backups no existe"
}

test_backup() {
    log_info "Ejecutando backup de prueba..."
    
    if [ -x "$SCRIPT_DIR/backup-db-only.sh" ]; then
        "$SCRIPT_DIR/backup-db-only.sh"
        log_info "âœ… Backup de prueba completado"
    else
        log_warn "Script de backup no encontrado o no es ejecutable"
        log_info "Ejecuta: chmod +x $SCRIPT_DIR/*.sh"
    fi
}

# Procesar argumentos
case "${1:-help}" in
    install)
        install_backups
        ;;
    remove)
        remove_backups
        ;;
    status)
        show_status
        ;;
    test)
        test_backup
        ;;
    help|--help|-h)
        show_help
        ;;
    *)
        echo "OpciÃ³n desconocida: $1"
        show_help
        exit 1
        ;;
esac
